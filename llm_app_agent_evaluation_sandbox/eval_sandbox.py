import os
import json
from openai import OpenAI

# 1. Initialize the OpenAI client
# Automatically fetches the key from the environment variable.
client = OpenAI(api_key=os.environ.get("OPENAI_API_KEY"))

# 2. Test Scenario Definition (Test Case)
test_scenario = {
    "prompt": "Calculate a route for a delivery van from Krakow to Tarnow via Bochnia. Use the A4 highway. The pure driving time is estimated at 60 minutes. Add a 15-minute buffer for unloading in Bochnia.",
    "expected_keywords": ["A4", "Bochnia", "Tarnow"],
    "max_allowed_time": 80,  # Minutes (Upper tolerance boundary)
    "min_allowed_time": 65   # Minutes (Lower tolerance boundary)
}

def get_agent_response(prompt: str) -> str:
    """Simulates the core AI Agent behavior."""
    response = client.chat.completions.create(
        model="gpt-4o-mini",
        messages=[
            {"role": "system", "content": "You are a logistics assistant. Respond concisely, providing the route and the total estimated travel time in minutes."},
            {"role": "user", "content": prompt}
        ],
        temperature=0.7 # Higher temperature introduces non-determinism for testing purposes
    )
    return response.choices[0].message.content

def evaluate_metrics(agent_output: str, scenario: dict) -> dict:
    """
    SYSTEM EVALUATION PIPELINE:
    Implements semantic validation, keyword coverage, and tolerance-based testing.
    """
    results = {
        "keyword_coverage": 0.0,
        "tolerance_check": "FAILED",
        "hallucination_judge": "UNKNOWN",
        "extracted_time": None
    }
    
    # METRIC 1: Keyword Coverage (Deterministic verification)
    found_keywords = [kw for kw in scenario["expected_keywords"] if kw.lower() in agent_output.lower()]
    results["keyword_coverage"] = len(found_keywords) / len(scenario["expected_keywords"])
    
    # METRIC 2: LLM-as-a-Judge (Data extraction & Hallucination detection)
    judge_prompt = f"""
    Analyze the following text generated by the Logistics Agent and extract:
    1. The total travel time in minutes (as an integer number only).
    2. Check if any cities outside the Krakow-Bochnia-Tarnow route appear in the text. (True/False)

    Agent Output: "{agent_output}"

    Return the result EXCLUSIVELY in the following JSON format:
    {{"extracted_time": int, "has_hallucinations": boolean}}
    """
    
    try:
        judge_response = client.chat.completions.create(
            model="gpt-4o-mini",
            messages=[{"role": "user", "content": judge_prompt}],
            response_format={"type": "json_object"}
        )
        judge_data = json.loads(judge_response.choices[0].message.content)
        
        extracted_time = judge_data.get("extracted_time")
        results["extracted_time"] = extracted_time
        results["hallucination_judge"] = "PASSED" if not judge_data.get("has_hallucinations") else "FAILED"
        
        # METRIC 3: Tolerance-Based Validation (Boundary testing for non-deterministic values)
        if extracted_time:
            if scenario["min_allowed_time"] <= extracted_time <= scenario["max_allowed_time"]:
                results["tolerance_check"] = "PASSED"
            else:
                results["tolerance_check"] = f"FAILED (Value {extracted_time} out of bounds [{scenario['min_allowed_time']}-{scenario['max_allowed_time']}])"
                
    except Exception as e:
        print(f"Evaluation Judge Error: {e}")
        
    return results

# --- EXECUTION PIPELINE ---
if __name__ == "__main__":
    print("Invoking AI Agent...")
    agent_response = get_agent_response(test_scenario["prompt"])
    print(f"\n[AGENT RESPONSE]:\n{agent_response}\n")
    
    print("Running Evaluation Framework (Pipeline)...")
    eval_results = evaluate_metrics(agent_response, test_scenario)
    
    print("\n[TEST RESULTS REVEALED]:")
    print(json.dumps(eval_results, indent=4, ensure_ascii=False))